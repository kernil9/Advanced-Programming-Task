package solution;

public class Pair<T1, T2> {
	public String T1;
	public int T2;
	public Pair(String s, int i)
	{
		T1 = s;
		T2 = i;
	}
  public String getT1() {
  return T1;}
  public void setT1(String t1) {
  T1 = t1;}
  public int getT2() {
  return T2;}
  public void setT2(int t2) {
  T2 = t2;}
	
}
